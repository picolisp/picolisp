/* Internal/transient hash */
unsigned long ihash(any x) {
   unsigned long g, h;
   word n;

   for (h = 0; isNum(x); x = cdr(numCell(x)))
      for (n = unDig(x); n; n >>= 8)
         g = (h = (h<<4) + (n&0xFF)) & 0xF0000000,  h = (h ^ g>>24) & ~g;
   return h % IHASH;
}

/* External hash */
unsigned long ehash(any x) {
   unsigned long h;
   word n;

   for (h = 0; isNum(x); x = cdr(numCell(x)))
      for (n = unDig(x); n; n >>= 11)
         h += n;
   return h % EHASH;
}

bool hashed(any s, any x) {
   while (isCell(x)) {
      if (s == car(x))
         return YES;
      x = cdr(x);
   }
   return NO;
}

any findHash(any s, any *p) {
   any x, y, *q, h;

   if (isCell(h = *p)) {
      x = s,  y = name(car(h));
      while (unDig(x) == unDig(y)) {
         x = cdr(numCell(x));
         y = cdr(numCell(y));
         if (!isNum(x) && !isNum(y))
            return car(h);
      }
      while (isCell(h = *(q = &cdr(h)))) {
         x = s,  y = name(car(h));
         while (unDig(x) == unDig(y)) {
            x = cdr(numCell(x));
            y = cdr(numCell(y));
            if (!isNum(x) && !isNum(y)) {
               *q = cdr(h),  cdr(h) = *p,  *p = h;
               return car(h);
            }
         }
      }
   }
   return NULL;
}

void unintern(any s, any *p) {
   any x;

   while (isCell(x = *p)) {
      if (s == car(x)) {
         *p = cdr(x);
         return;
      }
      p = &x->cdr;
   }
}

/* Get symbol name */
any name(any s) {
   for (s = tail1(s); isCell(s); s = cdr(s));
   return s;
}

// (name 'sym ['sym2]) -> sym
any doName(any ex) {
   any x, y, *p;
   unsigned long n;
   cell c1;

   x = cdr(ex),  data(c1) = EVAL(car(x));
   NeedSym(ex,data(c1));
   y = name(data(c1));
   if (!isCell(x = cdr(x)))
      return isNum(y)? consStr(y) : Nil;
   n = ihash(y);
   if (isNil(data(c1)) || isExt(data(c1)) || hashed(data(c1), Intern[n]))
      err(ex, data(c1), "Can't rename");
   Save(c1);
   x = EVAL(car(x));
   NeedSym(ex,x);
   unintern(data(c1), Transient + n);
   for (p = &tail(data(c1)); isCell(*p); p = &cdr(*p));
   *p = name(x);
   return Pop(c1);
}

/* Find or create single-char symbol */
any mkChar(int c) {
   if (c >= 0x80) {
      if (c < 0x800)
         c = 0xC0 | c>>6 & 0x1F  |  (0x80 | c & 0x3F) << 8;
      else if (c == TOP)
         c = 0xFF;
      else
         c = 0xE0 | c>>12 & 0x0F  |  (0x80 | c>>6 & 0x3F) << 8  |  (0x80 | c & 0x3F) << 16;
   }
   return consStr(box(c));
}

/* Make name */
any mkName(char *s) {
   int i;
   any nm;
   cell c1;

   i = 0,  Push(c1, nm = box(*(byte*)s++));
   while (*s)
      byteSym(*(byte*)s++, &i, &nm);
   return Pop(c1);
}

any intern(char *s) {
   any nm, x, *h;

   if (!*s)
      return Nil;
   nm = mkName(s);
   if (x = findHash(nm, h = Intern + ihash(nm)))
      return x;
   *h = cons(x = consStr(nm), *h);
   return x;
}

/* Make string */
any mkStr(char *s) {return s && *s? consStr(mkName(s)) : Nil;}

/* Get first byte of symbol name */
int firstByte(any s) {
   return !isNum(s = name(s))? 0 : unDig(s) & 0xFF;
}

int secondByte(any s) {
   return !isNum(s = name(s))? 0 : unDig(s) >> 8 & 0xFF;
}

bool isBlank(any x) {
   int c;

   if (!isSym(x))
      return NO;
   for (c = symChar(name(x)); c; c = symChar(NULL))
      if (c > ' ')
         return NO;
   return YES;
}

