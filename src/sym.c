// (chop 'any) -> lst
any doChop(any x) {
   int c;
   cell c1, c2;

   if (isCell(x = EVAL(cadr(x))))
      return x;
   if (!(c = symChar(name(x = xSym(x)))))
      return Nil;
   Push(c1, x);
   Push(c2, x = cons(mkChar(c), Nil));
   while (c = symChar(NULL))
      x = cdr(x) = cons(mkChar(c), Nil);
   drop(c1);
   return data(c2);
}

void pack(any x, int *i, any *nm, cell *p) {
   int c;
   cell c1;

   if (isCell(x))
      do
         pack(car(x), i, nm, p);
      while (isCell(x = cdr(x)));
   if (!isNil(x)) {
      if (isNum(x)) {
         Push(c1, x = numToSym(x, 0, 0, 0));
         c = symChar(name(x));
         if (*nm)
            charSym(c, i, nm);
         else
            Tuck(*p, c1, boxChar(c, i, nm));
         while (c = symChar(NULL))
            charSym(c, i, nm);
         drop(c1);
      }
      else if (c = symChar(name(x))) {
         if (*nm) {
            if (isExt(x))
               charSym('{', i, nm);
            charSym(c, i, nm);
         }
         else if (!isExt(x))
            Push(*p, boxChar(c, i, nm));
         else {
            Push(*p, boxChar('{', i, nm));
            charSym(c, i, nm);
         }
         while (c = symChar(NULL))
            charSym(c, i, nm);
         if (isExt(x))
            charSym('}', i, nm);
      }
   }
}

// (pack 'any ..) -> sym
any doPack(any x) {
   int i;
   any nm;
   cell c1, c2;

   x = cdr(x),  Push(c1, EVAL(car(x)));
   nm = NULL,  pack(data(c1), &i, &nm, &c2);
   while (isCell(x = cdr(x)))
      pack(data(c1) = EVAL(car(x)), &i, &nm, &c2);
   drop(c1);
   return nm? consStr(data(c2)) : Nil;
}

// (glue 'any 'lst) -> sym
any doGlue(any x) {
   int i;
   any nm;
   cell c1, c2, c3;

   x = cdr(x),  Push(c1, EVAL(car(x)));
   x = cdr(x),  Push(c2, x = EVAL(car(x)));
   if (!isCell(x)) {
      drop(c1);
      return x;
   }
   nm = NULL,  pack(car(x), &i, &nm, &c3);
   while (isCell(x = cdr(x))) {
      pack(data(c1), &i, &nm, &c3);
      pack(car(x), &i, &nm, &c3);
   }
   drop(c1);
   return nm? consStr(data(c3)) : Nil;
}

// (text 'any1 'any ..) -> sym
any doText(any x) {
   int c, n, i;
   any nm;
   any y = evSym(x = cdr(x));
   char *p, buf[bufSize(y)];
   cell c1;

   bufString(y, buf);
   if (!*(p = buf))
      return Nil;
   {
      cell arg[length(x = cdr(x))];

      for (n = 0;  isCell(x);  ++n, x = cdr(x))
         Push(arg[n], EVAL(car(x)));

      nm = NULL;
      do {
         if ((c = *p++) != '@') {
            if (nm)
               byteSym(c, &i, &nm);
            else
               i = 0,  Push(c1, nm = box(c & 0xFF));
         }
         else if (!(c = *p++))
            break;
         else if (c == '@') {
            if (nm)
               byteSym('@', &i, &nm);
            else
               i = 0,  Push(c1, nm = box('@'));
         }
         else if (c >= '1') {
            if ((c -= '1') > 8)
               c -= 7;
            if (n > c)
               pack(data(arg[c]), &i, &nm, &c1);
         }
      } while (*p);
      if (n)
         drop(arg[0]);
      else if (nm)
         drop(c1);
      return nm? consStr(data(c1)) : Nil;
   }
}

static bool pre(word n1, any y, word n2, any x) {
   for (;;) {
      if ((n1 & 0xFF) != (n2 & 0xFF))
         return NO;
      if ((n1 >>= 8) == 0) {
         if (!isNum(y = cdr(numCell(y))))
            return YES;
         n1 = unDig(y);
      }
      if ((n2 >>= 8) == 0) {
         if (!isNum(x = cdr(numCell(x))))
            return NO;
         n2 = unDig(x);
      }
   }
}

bool subStr(any y, any x) {
   word n;

   if (!isNum(y = name(y)))
      return YES;
   if (!isNum(x = name(x)))
      return NO;
   n = unDig(x);
   for (;;) {
      if (pre(unDig(y), y, n, x))
         return YES;
      if ((n >>= 8) == 0) {
         if (!isNum(x = cdr(numCell(x))))
            return NO;
         n = unDig(x);
      }
   }
}

// (pre? 'any1 'any2) -> any2 | NIL
any doPreQ(any x) {
   any y, z;
   cell c1;

   x = cdr(x),  Push(c1, evSym(x));
   x = cdr(x),  x = evSym(x);
   drop(c1);
   if (!isNum(y = name(data(c1))))
      return x;
   if (!isNum(z = name(x)))
      return Nil;
   return pre(unDig(y), y, unDig(z), z)? x : Nil;
}

// (sub? 'any1 'any2) -> any2 | NIL
any doSubQ(any x) {
   cell c1;

   x = cdr(x),  Push(c1, evSym(x));
   x = cdr(x),  x = evSym(x);
   drop(c1);
   return subStr(data(c1), x)? x : Nil;
}

