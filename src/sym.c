// (lup 'lst 'any) -> lst
// (lup 'lst 'any 'any2) -> lst
any doLup(any x) {
   int n;
   cell c1, c2, c3, c4, c5;

   x = cdr(x),  data(c1) = EVAL(car(x));
   if (!isCell(data(c1)))
      return data(c1);
   Save(c1);
   x = cdr(x),  Push(c2, EVAL(car(x)));  // from
   if (isCell(x = cdr(x))) {
      Push(c3, EVAL(car(x)));  // to
      Push(c4, Nil);  // tos
      Push(c5, Nil);  // result
      for (;;) {
         while (isCell(cddr(data(c1))) && car(data(c1)) != T && (!isCell(car(data(c1))) || compare(data(c3), caar(data(c1))) >= 0))
            x = data(c1),  data(c1) = cddr(x),  cddr(x) = data(c4),  data(c4) = x;
         for (;;) {
            if (isCell(car(data(c1))) && compare(data(c2), caar(data(c1))) <= 0) {
               if (compare(data(c3), caar(data(c1))) >= 0)
                  data(c5) = cons(car(data(c1)), data(c5));
               if (isCell(cadr(data(c1)))) {
                  x = data(c1),  data(c1) = cadr(x),  cadr(x) = data(c4),  data(c4) = symPtr(x);
                  break;
               }
            }
            for (;;) {
               if (isNil(data(c4))) {
                  drop(c1);
                  return data(c5);
               }
               if (isCell(data(c4))) {
                  x = data(c4),  data(c4) = cddr(x),  cddr(x) = data(c1),  data(c1) = x;
                  break;
               }
               else
                  x = cellPtr(data(c4)),  data(c4) = cadr(x),  cadr(x) = data(c1),  data(c1) = x;
            }
         }
      }
   }
   do {
      if (car(data(c1)) == T)
         data(c1) = cadr(data(c1));
      else if (!isCell(car(data(c1))))
         data(c1) = cddr(data(c1));
      else if (n = compare(data(c2), caar(data(c1))))
         data(c1) = n < 0? cadr(data(c1)) : cddr(data(c1));
      else {
         drop(c1);
         return car(data(c1));
      }
   } while (isCell(data(c1)));
   drop(c1);
   return Nil;
}

void put(any x, any key, any val) {
   any y, z;

   if (isCell(y = tail1(x))) {
      if (isCell(car(y))) {
         if (key == cdar(y)) {
            if (isNil(val))
               Tail(x, cdr(y));
            else if (val == T)
               car(y) = key;
            else
               caar(y) = val;
            return;
         }
      }
      else if (key == car(y)) {
         if (isNil(val))
            Tail(x, cdr(y));
         else if (val != T)
            car(y) = cons(val,key);
         return;
      }
      while (isCell(z = cdr(y))) {
         if (isCell(car(z))) {
            if (key == cdar(z)) {
               if (isNil(val))
                  cdr(y) = cdr(z);
               else {
                  if (val == T)
                     car(z) = key;
                  else
                     caar(z) = val;
                  cdr(y) = cdr(z),  cdr(z) = tail1(x),  Tail(x, z);
               }
               return;
            }
         }
         else if (key == car(z)) {
            if (isNil(val))
               cdr(y) = cdr(z);
            else {
               if (val != T)
                  car(z) = cons(val,key);
               cdr(y) = cdr(z),  cdr(z) = tail1(x),  Tail(x, z);
            }
            return;
         }
         y = z;
      }
   }
   if (!isNil(val))
      Tail(x, cons(val==T? key : cons(val,key), tail1(x)));
}

any get(any x, any key) {
   any y, z;

   if (!isCell(y = tail1(x)))
      return Nil;
   if (!isCell(car(y))) {
      if (key == car(y))
         return T;
   }
   else if (key == cdar(y))
      return caar(y);
   while (isCell(z = cdr(y))) {
      if (!isCell(car(z))) {
         if (key == car(z)) {
            cdr(y) = cdr(z),  cdr(z) = tail1(x),  Tail(x, z);
            return T;
         }
      }
      else if (key == cdar(z)) {
         cdr(y) = cdr(z),  cdr(z) = tail1(x),  Tail(x, z);
         return caar(z);
      }
      y = z;
   }
   return Nil;
}

any prop(any x, any key) {
   any y, z;

   if (isCell(y = tail1(x))) {
      if (!isCell(car(y))) {
         if (key == car(y))
            return key;
      }
      else if (key == cdar(y))
         return car(y);
      while (isCell(z = cdr(y))) {
         if (!isCell(car(z))) {
            if (key == car(z)) {
               cdr(y) = cdr(z),  cdr(z) = tail1(x),  Tail(x, z);
               return key;
            }
         }
         else if (key == cdar(z)) {
            cdr(y) = cdr(z),  cdr(z) = tail1(x),  Tail(x, z);
            return car(z);
         }
         y = z;
      }
   }
   Tail(x, cons(y = cons(Nil,key), tail1(x)));
   return y;
}

